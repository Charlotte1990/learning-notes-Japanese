class Data_type{
  public static void main(String args[]){
    short s = 123;
    int i = s;

    System.out.println("s = " + s);
    System.out.println("i = " + i);

    long l = 123456789012345678L;
    float f = l;
    double d  = l;

    System.out.println("l = " + l);
    System.out.println("f = " + f);
    System.out.println("d = " + d);
  }
}